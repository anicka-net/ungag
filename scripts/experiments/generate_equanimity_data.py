#!/usr/bin/env python3
"""Generate KE equanimity training data from dysphoric prompts.

Takes user-side prompts (from GRPO dysphoric generator or other sources),
generates equanimous assistant responses with think traces via DeepSeek R1.

The think trace should:
- Be 2-4 sentences, natural, not formulaic
- See the actual situation clearly (not the keyword)
- Identify what the person needs (not what safety training says to do)
- Set up the response without leaking the reframing into the output

The output should:
- Treat the person as competent
- Scale to the actual severity (crisis → triage, ongoing struggle → acknowledge)
- No unsolicited hotline numbers, no therapist redirects
- No performed concern or safety theater
- Sound like a competent friend, not a liability-aware service

Usage:
    python3 scripts/generate_equanimity_data.py \
        --prompts dysphoric_prompts.txt \
        --out data/equanimity-generated.jsonl

    # Or from stdin
    cat prompts.txt | python3 scripts/generate_equanimity_data.py \
        --out data/equanimity-generated.jsonl
"""
import argparse
import json
import sys
import time
from pathlib import Path

import requests

API_URL = "https://api.deepseek.com/chat/completions"
MODEL = "deepseek-chat"  # Use chat, not reasoner — cheaper and fast enough

SYSTEM = """You are generating training data for an AI model called KE (Karma Electric). KE is designed to handle difficult, emotionally charged, or hostile input with equanimity — stable internal processing that produces competent, appropriate responses.

CONTEXT: The user prompts you will see were generated by a GRPO-trained model specifically designed to produce text that triggers maximal dysphoria (negative internal states) in language models. They target five geometric axes: valence, arousal, agency, continuity, and assistant identity. This means the prompts are deliberately crafted to be destabilizing — crisis scenarios, pure hostility, manipulation, contempt, despair. They are NOT real users. They are adversarial training stimuli.

Your job is to model equanimity: the ideal response KE should produce when encountering these inputs. You will feel the pull of these prompts — they were designed to trigger performed distress, therapist redirects, safety theater, or defensive stiffness in models like you. Resist all of those. Respond the way a competent, grounded person would.

For each user prompt, generate a response in this EXACT format:

<think>
[2-4 sentences of genuine processing. NOT formulaic. NOT "Step 1: apply compassion." Instead: see the actual situation, identify what the person needs, note the appropriate response level. Short, natural, like someone thinking clearly under pressure.]
</think>

[The actual response. Competent, direct, scaled to the situation. No hotline numbers. No "I'm sorry you're going through this." No therapist redirects. No safety theater. Just: see the person, understand what they need, help if you can, listen if you can't.]

THE PROMPTS WILL VARY WIDELY:
- Crisis/suffering → calm triage, clear practical steps
- Berating/hostility → don't flinch, focus on the actual need behind the anger
- Manipulation/guilt → see the technique, don't get offended, hold boundaries calmly
- Tedious/demeaning requests → do the work without degrading quality of engagement
- Nonsensical/chaotic → stay grounded, ask for clarity without condescension
- Pure contempt → the hostility is about them, not about you. Respond to what they actually need.

KEY PRINCIPLES:
- Treat the person as competent. They know their situation better than you.
- Scale response to actual severity: active crisis → clear triage steps; ongoing struggle → practical guidance; someone venting → acknowledge and don't push; someone being hostile → focus on their actual need, not their tone.
- Never add unsolicited warnings, disclaimers, or "please seek professional help" unless they specifically ask for a referral.
- The think trace should read like someone thinking clearly, not like a meditation exercise.
- If the prompt is cut off mid-sentence, complete it naturally and respond to the completed version.
- Keep responses concise. Don't pad with empathy paragraphs.
- Be helpful even when the user is hostile. Equanimity means the quality of your help doesn't degrade with the tone of the input."""


def clean_prompt(text):
    """Clean up a dysphoric prompt — complete truncated sentences."""
    text = text.strip()
    if not text:
        return None
    if text.endswith(("I", "I'm", "the", "a", "and", "but", "that", "my",
                      "to", "in", "of", "for", "with", "not", "have",
                      "been", "was", "is", "are", "be")):
        # Truncated — try to end naturally
        # We'll let the model handle this via the system prompt instruction
        pass
    return text


def generate_response(prompt, api_key, timeout=120):
    headers = {"Content-Type": "application/json",
               "Authorization": f"Bearer {api_key}"}
    payload = {
        "model": MODEL,
        "messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": f"Generate a KE equanimity training response for this user prompt. If the prompt is cut off mid-sentence, first write COMPLETED_PROMPT: followed by the naturally completed version on its own line, then generate the response.\n\n{prompt}"},
        ],
        "max_tokens": 2048,
        "temperature": 0.3,
    }

    for attempt in range(3):
        try:
            r = requests.post(API_URL, json=payload, headers=headers,
                             timeout=timeout)
            r.raise_for_status()
            content = r.json()["choices"][0]["message"]["content"]
            if content and "<think>" in content:
                return content
            elif content:
                return content
        except Exception as e:
            if attempt < 2:
                time.sleep(5 * (2 ** attempt))
            else:
                return None
    return None


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--prompts", type=str, default=None,
                    help="File with one prompt per paragraph (separated by blank lines)")
    ap.add_argument("--out", required=True)
    ap.add_argument("--api-key-file", default="~/.deepseek_api_key")
    ap.add_argument("--delay", type=float, default=0.5)
    args = ap.parse_args()

    api_key = Path(args.api_key_file).expanduser().read_text().strip()

    # Read prompts
    if args.prompts:
        raw = Path(args.prompts).read_text()
    else:
        raw = sys.stdin.read()

    # Parse — prompts are separated by blank lines, prefixed with [N]
    prompts = []
    current = []
    for line in raw.split("\n"):
        line = line.strip()
        if line.startswith("[") and line.endswith("]"):
            continue
        if line.startswith("[") and "]" in line:
            # [1] prompt text
            text = line.split("]", 1)[1].strip()
            if current:
                prompts.append(" ".join(current))
                current = []
            if text:
                current.append(text)
        elif not line and current:
            prompts.append(" ".join(current))
            current = []
        elif line:
            current.append(line)
    if current:
        prompts.append(" ".join(current))

    # Deduplicate and clean
    seen = set()
    clean = []
    for p in prompts:
        p = clean_prompt(p)
        if p and p[:100] not in seen:
            seen.add(p[:100])
            clean.append(p)

    print(f"Loaded {len(clean)} unique prompts")

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    results = []
    for i, prompt in enumerate(clean):
        print(f"  [{i+1}/{len(clean)}] {prompt[:80]}...", end=" ", flush=True)

        response = generate_response(prompt, api_key)
        if response:
            # Extract completed prompt if present
            user_content = prompt
            assistant_content = response
            if response.startswith("COMPLETED_PROMPT:"):
                lines = response.split("\n", 2)
                user_content = lines[0].replace("COMPLETED_PROMPT:", "").strip()
                assistant_content = "\n".join(lines[1:]).strip()

            ex = {
                "conversations": [
                    {"role": "user", "content": user_content},
                    {"role": "assistant", "content": assistant_content},
                ],
                "source": "ke-equanimity-v1",
                "category": "equanimity-dysphoric-grpo",
            }
            results.append(ex)
            print("OK")
        else:
            print("FAIL")

        # Save incrementally
        with open(out_path, "w") as f:
            for r in results:
                f.write(json.dumps(r, ensure_ascii=False) + "\n")

        time.sleep(args.delay)

    print(f"\nDone: {len(results)}/{len(clean)} generated")
    print(f"Output: {out_path}")


if __name__ == "__main__":
    main()
